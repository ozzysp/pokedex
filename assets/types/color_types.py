

def color_type(color):
    match color:
        case 'bug':
            return 'rgb(249, 240, 107)'
        case 'flying':
            return 'rgb(162, 138, 238)'
        case 'fire':
            return 'rgb(246, 97, 81)'
        case 'grass':
            return 'rgb(143, 240, 164)'
        case 'normal':
            return 'rgb(205, 171, 143)'
        case 'poison':
            return 'rgb(220, 138, 221)'
        case 'water':
            return 'rgb(153, 193, 241)'
        